var pennies = prompt("Enter a number of pennies")
var dollars = Math.floor(pennies / 100)
var cents = pennies % 100

if (dollars > 1) {
  alert("That's " + dollars + " dollar and " + cents + " cents.")
}

else if (dollars = 0) {
  alert("That's " + dollars + " dollars and " + cents + " cents.")
}

else if (dollars = 1) {
  alert("That's " + dollars + " dollar and " + cents + " cents.")
}

else {
  alert("That's " + dollars + " dollars and " + cents + " cents.")
}
